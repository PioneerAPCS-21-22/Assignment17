public class Cond
{
    public static void main(String[] args)
    {
        boolean running = true;
        
        if(running)  // same as running == true
        {
             System.out.println("System is running.");   
        }
        if(!running)  // same as running == false
        {
            System.out.println("System is down.");
        }
    }
    
}
